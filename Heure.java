public class Heure {
	private int heures;
	private int minutes;

	public Heure(int heures, int minutes){

		this.heures = heures;
		this.minutes = minutes;

	};
	public int getHeures(){

		return this.heures;
	};

	public int getMinutes(){

		return this.minutes;
	};

	public void setHeures(int heures){

		this.heures = heures;
	};

	public void setMinutes(int minutes){

		this.minutes = minutes;
	};


};
